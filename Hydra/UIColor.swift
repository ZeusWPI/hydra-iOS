import Foundation

extension UIColor {

    class func hydraTintcolor() -> UIColor {
        // #204E80
        return UIColor(red: 0.126, green:0.304, blue:0.500, alpha:1.000)
    }

    class func hydraBackgroundColor() -> UIColor {
        // #CED6E0
        return UIColor(red: 0.807, green: 0.840, blue: 0.878, alpha: 1.000)
    }

    class func SKOBackgroundColor() -> UIColor {
        // #009ade
        return UIColor(red: 0, green: 0.604, blue: 0.871, alpha: 1.000)
    }
}